export function getSymbolById(id: string): AdinkraSymbol | undefined {
  return ADINKRA_SYMBOLS.find((s) => s.id === id);
}

export function getSymbolCatalogForPrompt(): string {
  return ADINKRA_SYMBOLS.map(
    (s) =>
      `[${s.id}] ${s.name} — "${s.literal}"\n` +
      `  Core: ${s.coreMeaning}\n` +
      `  Also: ${s.secondaryMeanings.join("; ")}\n` +
      `  Tags: ${s.emotionalTags.join(", ")}\n` +
      `  Domains: ${s.lifeDomains.join(", ")}\n` +
      `  Sensitive: ${s.sensitive ? "YES — " + s.usageCautions : "No"}\n` +
      `  Incompatible with: ${s.incompatibleSymbols.length ? s.incompatibleSymbols.join(", ") : "none"}`
  ).join("\n\n");
}
